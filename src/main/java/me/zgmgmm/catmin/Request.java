    public void setStartLine(byte[] startLine, int offset, int length) {
        if (length > this.startLine.length)
            this.startLine = Arrays.copyOfRange(startLine, offset, length);
        else
            System.arraycopy(startLine, offset, this.startLine, 0, length);
        //parse
        int i = 0;
        while (i < length - offset && this.startLine[i++] != ' ');
        methodEnd = i-1;
        while (i < length - offset && this.startLine[i++] != ' ');
        uriEnd = i-1;
        schemeEnd = length - offset;
    }

